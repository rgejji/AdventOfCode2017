package main

import (
	"fmt"
	"sort"
	"strings"
)

type direction = int
type element = int

const (
	//WEST is left
	WEST direction = iota
	//NORTH is up
	NORTH
	//EAST is right
	EAST
	//SOUTH is down
	SOUTH
)
const (
	//EMPTY is space
	EMPTY element = iota
	//H -
	H
	//V |
	V
	//FS /
	FS
	//BS \
	BS
	//PLUS +
	PLUS
)

//Cart is a cart object
type Cart struct {
	x         int
	y         int
	direction direction
	nextTurn  direction
}

//Loc is grid location, it has a cart and underlying value
type Loc struct {
	value   element
	hasCart *Cart
}

//ByCarts implements sort.Interface based on the location
type ByCarts []*Cart

func (c ByCarts) Len() int { return len(c) }
func (c ByCarts) Less(i, j int) bool {
	if c[i].y < c[j].y {
		return true
	}
	if c[i].y == c[j].y && c[i].x < c[j].x {
		return true
	}
	return false

}
func (c ByCarts) Swap(i, j int) { c[i], c[j] = c[j], c[i] }

var grid [][]*Loc
var carts []*Cart

//Prints a grid with carts
func printGrid() {
	for _, row := range grid {
		for _, loc := range row {
			if loc.hasCart != nil {
				fmt.Printf("C")
				continue
			}
			switch loc.value {
			case H:
				fmt.Printf("-")
			case V:
				fmt.Printf("|")
			case FS:
				fmt.Print("/")
			case BS:
				fmt.Printf("\\")
			case PLUS:
				fmt.Printf("+")
			case EMPTY:
				fmt.Printf(" ")
			default:
				fmt.Printf("\nError: Unrecognized value %v\n", loc.value)
				return
			}
		}
		fmt.Printf("\n")
	}
	fmt.Printf("\n")
}

//Prints the cart locations
func printCarts() {
	for _, c := range carts {
		fmt.Printf("Cart at y=%d, x=%d with direction %v and next turn %v\n", c.y, c.x, c.direction, c.nextTurn)
	}
	fmt.Printf("\n")

}

//Read the input of the carts
func readInput() {
	rowSlice := strings.Split(inputStr, "\n")
	for y, row := range rowSlice {
		newGridRow := make([]*Loc, len(row), len(row))
		for x, r := range row {
			var cart *Cart
			var e element
			switch r {
			case '-':
				e = H
			case '|':
				e = V
			case '+':
				e = PLUS
			case '/':
				e = FS
			case '\\':
				e = BS
			case '>':
				e = H
				cart = &Cart{
					x:         x,
					y:         y,
					direction: EAST,
				}
				carts = append(carts, cart)
			case '<':
				e = H
				cart = &Cart{
					x:         x,
					y:         y,
					direction: WEST,
				}
				carts = append(carts, cart)
			case '^':
				e = V
				cart = &Cart{
					x:         x,
					y:         y,
					direction: NORTH,
				}
				carts = append(carts, cart)
			case 'v':
				e = V
				cart = &Cart{
					x:         x,
					y:         y,
					direction: SOUTH,
				}
				carts = append(carts, cart)
			}
			newGridRow[x] = &Loc{
				value:   e,
				hasCart: cart,
			}
		}

		grid = append(grid, newGridRow)
	}
	return
}

//Organize the carts with primary value vertical order, secondary value horizontal order
func sortCarts() {
	sort.Sort(ByCarts(carts))
}

//Updates the carts, checking for collisions during each update
func updateCartsInOrder() (int, int) {
	for _, cart := range carts {
		currX := cart.x
		currY := cart.y

		//Get location
		loc := grid[currY][currX]

		//Figure out next location
		nextX := currX
		nextY := currY
		switch cart.direction {
		case WEST:
			nextX--
		case EAST:
			nextX++
		case NORTH:
			nextY--
		case SOUTH:
			nextY++
		}
		nextLoc := grid[nextY][nextX]

		//Check for collision
		if nextLoc.hasCart != nil {
			return nextY, nextX
		}
		//fmt.Printf("Moving from (y,x)=(%d, %d) to (%d, %d)\n", currY, currX, nextY, nextX)

		//Move cart on grid and slice
		cart.x = nextX
		cart.y = nextY
		loc.hasCart = nil
		nextLoc.hasCart = cart

		//Update direction
		switch nextLoc.value {
		case FS:
			switch cart.direction {
			case WEST:
				cart.direction = SOUTH
			case SOUTH:
				cart.direction = WEST
			case EAST:
				cart.direction = NORTH
			case NORTH:
				cart.direction = EAST
			default:
				fmt.Printf("ERROR: illegal move %d for cart %+v on new location %+v\n", cart.direction, cart, nextLoc)
				return 0, 0
			}
		case BS:
			switch cart.direction {
			case EAST:
				cart.direction = SOUTH
			case SOUTH:
				cart.direction = EAST
			case NORTH:
				cart.direction = WEST
			case WEST:
				cart.direction = NORTH
			default:
				fmt.Printf("ERROR: illegal move %d for cart %+v on new location %+v\n", cart.direction, cart, nextLoc)
				return 0, 0
			}
		case PLUS:
			cart.direction = (cart.direction + cart.nextTurn - 1 + 4) % 4
			cart.nextTurn = (cart.nextTurn + 1) % 3
		}
	}

	return -1, -1
}

func main() {
	maxNumTurns := 1000
	readInput()

	//printGrid()
	printCarts()
	for n := 0; n < maxNumTurns; n++ {
		sortCarts()
		y, x := updateCartsInOrder()
		if y >= 0 {
			fmt.Printf("Found collision at time n=%d at (X,Y) = (%d,%d)\n", n, x, y)
			break
		}
		//printGrid()
		printCarts()
	}
}

